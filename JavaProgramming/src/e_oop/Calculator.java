package e_oop;

public class Calculator {

	double add(int a, int b){
		return a + b;
	}
	
	double subtract(double a, double b){
		return a - b;
	}
	
	double multiplication(double a, double b){
		return a * b;
	}
	
	double division(double a, double b){
	
		return a / b; 
		
	}
	
	double remainder(double a, double b){
		return a % b;
	}
	
}
